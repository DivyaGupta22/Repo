package com.divya.oracle;

public class Multiplication {
	
	public void mul(int a,int b)
	{
		System.out.println("Multiplication of 2 int number :"+ a*b); 
	}

	public void mul (int a,int b,int c)
	{
		System.out.println("Multiplication of 3 int number :"+ a*b*c);
	
	}
	
	public void mul(int a, float b) 
{ 
	System.out.println("Multiplication of int and float number :"+ a*b);	
}

}

